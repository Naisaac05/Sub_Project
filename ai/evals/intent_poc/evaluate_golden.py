# -*- coding: utf-8 -*-
"""
Evaluate intent PoC classifiers on ../golden_dataset.jsonl.

The golden dataset uses runtime intent labels plus optional sub-intents.
This script maps those labels into the PoC 10-class taxonomy before scoring.
"""
import argparse
import json
import pathlib
from collections import Counter, defaultdict

from classifiers import ABBR, CLASSES, TARGET_DESC, get_classifier
from evaluate import prf

HERE = pathlib.Path(__file__).parent
DEFAULT_DATA = HERE.parent / "golden_dataset.jsonl"

GOLDEN_SUB_INTENT_MAP = {
    ("concept_definition", "comparison"): "COMPARISON",
    ("concept_definition", "practical"): "PRACTICAL_USAGE",
    ("concept_definition", "definition"): "CONCEPT_DEFINITION",
    ("concept_definition", "related"): "CONCEPT_DEFINITION",
    ("wrong_answer_explanation", "explanation"): "WRONG_ANSWER_REASON",
    ("follow_up", "follow_up"): "FOLLOW_UP",
}

GOLDEN_INTENT_DEFAULT = {
    "concept_definition": "CONCEPT_DEFINITION",
    "wrong_answer_explanation": "WRONG_ANSWER_REASON",
    "follow_up": "FOLLOW_UP",
    "general_question": "UNKNOWN",
}


def expected_label_from_golden(row):
    key = (row.get("expected_intent"), row.get("expected_sub_intent"))
    return GOLDEN_SUB_INTENT_MAP.get(
        key,
        GOLDEN_INTENT_DEFAULT.get(row.get("expected_intent"), "UNKNOWN"),
    )


def main():
    ap = argparse.ArgumentParser(description="Evaluate intent PoC classifier on golden_dataset.jsonl")
    ap.add_argument("--classifier", default="current", help="classifier name from classifiers.py")
    ap.add_argument("--dataset", type=pathlib.Path, default=DEFAULT_DATA)
    ap.add_argument("--out", type=pathlib.Path, default=None)
    args = ap.parse_args()

    out_path = args.out or (HERE / f"REPORT_golden_{args.classifier}.md")
    predict = get_classifier(args.classifier)
    rows = [json.loads(line) for line in args.dataset.read_text(encoding="utf-8").splitlines() if line.strip()]
    rows = [row for row in rows if row.get("expected_intent")]

    confusion = defaultdict(Counter)
    support = Counter()
    predicted_total = Counter()
    correct = Counter()
    misses = []

    for row in rows:
        exp = expected_label_from_golden(row)
        try:
            pred = predict(row["question"])
        except Exception as exc:  # noqa: BLE001
            pred = f"(error:{type(exc).__name__})"
        ok = pred == exp
        confusion[exp][pred] += 1
        support[exp] += 1
        predicted_total[pred] += 1
        correct[exp] += 1 if ok else 0
        if not ok:
            misses.append((row.get("id", ""), exp, pred, row["question"]))

    total = len(rows)
    total_correct = sum(correct.values())
    overall_acc = total_correct / total if total else 0.0
    macro_f1 = sum(prf(support[c], predicted_total[c], correct[c])[2] for c in CLASSES) / len(CLASSES)

    report = []
    report.append(f"# Intent Golden Evaluation - classifier=`{args.classifier}`\n")
    report.append("> Auto-generated by `python evals/intent_poc/evaluate_golden.py`.\n")
    report.append(f"- dataset: `{args.dataset.name}` ({total} rows with expected_intent)")
    report.append(f"- target: {TARGET_DESC.get(args.classifier, args.classifier)}")
    report.append(f"- accuracy: {overall_acc:.1%} ({total_correct}/{total})")
    report.append(f"- macro-F1: {macro_f1:.3f}\n")

    report.append("## Per-Class Metrics\n")
    report.append("| intent | support | correct | precision | recall | F1 |")
    report.append("|---|---:|---:|---:|---:|---:|")
    for cls in CLASSES:
        p, recall, f1 = prf(support[cls], predicted_total[cls], correct[cls])
        report.append(f"| {cls} | {support[cls]} | {correct[cls]} | {p:.2f} | {recall:.2f} | {f1:.2f} |")
    report.append("")

    report.append("## Confusion Matrix\n")
    report.append("| expected\\predicted | " + " | ".join(ABBR[c] for c in CLASSES) + " |")
    report.append("|" + "---|" * (len(CLASSES) + 1))
    for exp in CLASSES:
        cells = [str(confusion[exp][pred]) if confusion[exp][pred] else "." for pred in CLASSES]
        report.append(f"| **{ABBR[exp]}** | " + " | ".join(cells) + " |")
    report.append("")

    report.append("## Misses\n")
    report.append("| id | expected | predicted | question |")
    report.append("|---|---|---|---|")
    for row_id, exp, pred, question in misses[:30]:
        report.append(f"| {row_id} | {exp} | {pred} | {question} |")
    report.append("")

    out_path.write_text("\n".join(report), encoding="utf-8")
    print(
        f"classifier={args.classifier} dataset=golden total={total} "
        f"acc={overall_acc:.3f} macro_f1={macro_f1:.3f}"
    )
    print(f"report -> {out_path}")


if __name__ == "__main__":
    main()
